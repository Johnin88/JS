// Leia dois numeros e informe o maior deles. 

let num1 = Number(prompt('Informe o primeiro numero: '))
let num2 = Number(prompt('Informe o segundo numero: '))

if(num1 > num2){
    alert('O primeiro numero é maior que o segundo')
}
else if(num2 > num1){
    alert('O segundo numero é maior que o primeiro')
}

